    int32_t r = 0, b = 0, g = 0;
    r = (buffer[4] - '0') * 100 + (buffer[5] - '0') * 10 + buffer[6] - '0';
    g = (buffer[8] - '0') * 100 + (buffer[9] - '0') * 10 + buffer[10] - '0';
    b = (buffer[12] - '0') * 100 + (buffer[13] - '0') * 10 + buffer[14] - '0';
    TIM3->CCR2 = r;
    TIM3->CCR3 = b;
    TIM3->CCR4 = g;
